// Translation context and hooks
let currentLanguage: Language = 'English';
let translations = buttonTranslations;

export const getFullLanguageName = (shortName: Language): string => {
  return languageNames[shortName] || shortName;
};

export const loadTranslations = async (language: Language) => {
  currentLanguage = language;
  return buttonTranslations;
};

export const initializeTranslations = () => {
  const savedLanguage = localStorage.getItem('selectedLanguage') || 'English';
  currentLanguage = savedLanguage as Language;
  return loadTranslations(savedLanguage as Language);
};

export const useTranslation = () => {
  const [language, setLanguage] = useState<Language>(currentLanguage);

  const getTranslation = useCallback((key: string, defaultValue = '') => {
    // Use type assertion to handle string keys
    if (!(key in translations)) {
      return defaultValue;
    }
    // Use type assertion to safely access translations with string keys
    return (translations as any)[key][language] || (translations as any)[key]['English'] || defaultValue;
  }, [language]);

  const changeLanguage = useCallback(async (newLanguage: Language) => {
    currentLanguage = newLanguage;
    localStorage.setItem('selectedLanguage', newLanguage);
    translations = await loadTranslations(newLanguage);
    setLanguage(newLanguage);
  }, []);

  return {
    language,
    changeLanguage,
    getTranslation,
    getFullLanguageName
  };
};
